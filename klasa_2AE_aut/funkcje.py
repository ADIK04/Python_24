def ResztaZDzielenia(a,b):
    return a % b

a = int(input('Podaj a: '))
b = int(input('Podaj b: '))

print('WYnik reszty z dzielenia: ', (ResztaZDzielenia(a,b)))

def Dodawanie(a,b):
    return a + b

a = int(input('Podaj a: '))
b = int(input('Podaj b: '))
