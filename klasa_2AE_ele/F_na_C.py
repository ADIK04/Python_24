# Przeliczanie Fahrenheita na Celsiusa
f = int(input('Temperatura w skali Fahrenheita: '))
print(f, ' stopni F to ', (f-32) * 5/9, ' stopni C.')

# Przeliczanie Celsiusa na Fahrenheita  
C = float(input('Temperatura w skali Celciusza: '))
print(C, ' stopni C to ', 32 + (C * 9/5), ' stopni F.')

